require "Day3"

describe Day3 do

    context "test examples" do
      it "returns accurate number of trees" do
        test1 = "
        .........#....#.###.........##.
        ..###.#......#......#.......##.
        ##....#.#.......#.....#........
        #........#..........#.#...#...#
        #....###...##.....#........#...
        ###..............##..#.....#...
        .................##.#..........
        "
        expect(Day3.run(test1)).to eq(5)
      end

      it "returns 0 if no trees" do
            test2 = "
            ..............#.#.#..#..#..#...
            .#.#.#....#.........#.........#
            ..#.#....##..#...#.....#..##..#
            ............#.....#.........##.
            ...#.#..#..........#.##.....#..
            #......#...##.......###........
            .....#....#.#..............#...
            .....#.......#..##.###.....#.#.
            .#.....#....##.#......##......#
            "
            expect(Day3.run(test2)).to eq(2)
      end
    end
end